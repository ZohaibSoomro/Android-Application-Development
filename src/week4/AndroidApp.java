package week4;

public class AndroidApp {
    double size;
    String name;
    String developer;
    boolean isFree;
    double version;

    public AndroidApp() {

    }

    public AndroidApp(double size, String name, String developer, boolean isFree, double version) {
        this.size = size;
        this.name = name;
        this.developer = developer;
        this.isFree = isFree;
        this.version = version;
    }
}
